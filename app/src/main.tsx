import { ClerkProvider } from '@clerk/clerk-react'
import { QueryClientProvider } from '@tanstack/react-query'
import { createRouter, RouterProvider } from '@tanstack/react-router'
import { StrictMode } from 'react'
import { createRoot } from 'react-dom/client'

import { queryClient } from './api/queryClient'
import { ALL_QUESTIONS } from './data/questions'
import './index.css'
import { routeTree } from './routeTree.gen'

// Expose the question bank on `window` so Playwright e2e tests can resolve
// the correct answer for the rendered prompt without hard-coding it. The
// bank is already in the bundle (anyone with DevTools can inspect it), so
// this just gives it a stable global handle. Not a security concern — the
// dataset itself is what we care about, and that's bundled either way.
;(window as unknown as { __HPC_BANK__: typeof ALL_QUESTIONS }).__HPC_BANK__ = ALL_QUESTIONS

// `routeTree.gen.ts` is auto-generated by @tanstack/router-plugin from the
// files in src/routes. Don't edit it; the plugin rewrites it on every dev
// run and on `pnpm build`.
const router = createRouter({
  routeTree,
  // Default search-param parser; safe in case any route uses `?dev=1`.
  defaultPreload: 'intent',
})

declare module '@tanstack/react-router' {
  interface Register {
    router: typeof router
  }
}

const PUBLISHABLE_KEY = import.meta.env.VITE_CLERK_PUBLISHABLE_KEY as string | undefined
if (!PUBLISHABLE_KEY) {
  throw new Error(
    'Missing VITE_CLERK_PUBLISHABLE_KEY — copy app/.env.example to .env.local and set it',
  )
}

const rootEl = document.getElementById('root')
if (!rootEl) throw new Error('Missing #root element in index.html')

createRoot(rootEl).render(
  <StrictMode>
    <ClerkProvider
      publishableKey={PUBLISHABLE_KEY}
      // Map Clerk's redirects to TanStack Router routes so all auth
      // navigations stay inside our SPA shell.
      signInUrl="/sign-in"
      signUpUrl="/sign-up"
      signInFallbackRedirectUrl="/"
      signUpFallbackRedirectUrl="/"
    >
      <QueryClientProvider client={queryClient}>
        <RouterProvider router={router} />
      </QueryClientProvider>
    </ClerkProvider>
  </StrictMode>,
)
